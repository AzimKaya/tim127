package A_Eylul_H_2;

public class e16_whileLoop {
    public static void main(String[] args) {

        int sayi=1;

        while ( sayi<=10 ){
            System.out.print(sayi +" ");
            sayi++;
        }

        System.out.println(" ==============");


        for (int i = 1; i <=10 ; i++) {
            System.out.print(i+" ");

        }


    }
}
